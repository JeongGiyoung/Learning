# 주석입니다

# print() : 문자열 표현방법

print('Hello, World')
print("Hello, World")

# 숫자 연산 출력
print(3+4)

# 변수 사용

a = '안녕하세요.'
b = '반갑습니다!!'
print(a)
print(b)
print(a, b)
print(a, b, '정말?')

# 수학연산

print(5 + 6)
print(5 - 6)
print(5 * 6)
print(5 / 3)

# // : 나누기할 때 몫 값만 출력

print(5 // 3)

# % : 나누기할 때 몫을 빼고 나머지 값만 출력

print(5 % 3) 

# ** : 제곱 출력

print(2 ** 10) 

# int() : 정수 값으로 출력

print(int(5 / 3)) 

print(int('10'))

# 형변환 안됨

#print(int('십')) 
#print(int('ten'))

# float() : 실수 표현

print(float(5))

# type() : 형식 확인

print(type(10))
print(type(10.0))

# () : 괄호 (연산자 우선순위)

print( (2 + 3) * 5 )
print( 2 + ( 3 * 5 ))